    private void jButtonReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReserveActionPerformed
        String faculty = jComboBoxFacultyReserve.getSelectedItem().toString();
        Date date = Date.valueOf(jComboBoxDateReserve.getSelectedItem().toString());
        int seats = Integer.parseInt(jTextFieldSeatsReserve.getText());
        boolean result = ReservationQueries.addReservationEntry(faculty, date, seats);
        if (result == false) {
            WaitlistQueries.addWaitlistEntry(faculty, date, seats);
            fillWaitlist();
            
        }
        
    }//GEN-LAST:event_jButtonReserveActionPerformed

    private void jButtonStatusReservationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStatusReservationActionPerformed
        Date date = Date.valueOf(jComboBoxDateStatus.getSelectedItem().toString());
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationsByDate(date);
        listStatusReservation.removeAllElements();
        for (int i = 0; i < reservations.size(); i++) {
            listStatusReservation.addElement("Faculty: " + reservations.get(i).getFaculty() + " Room: " + reservations.get(i).getRoom() + "   Date: " + reservations.get(i).getDate());
        }
    }//GEN-LAST:event_jButtonStatusReservationActionPerformed

    private void jButtonRoomSeatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRoomSeatsActionPerformed
        String room = jTextFieldAddRoom.getText();
        int seats = Integer.parseInt(jTextFieldAddSeats.getText());
        RoomQueries.addRoom(room, seats);
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        for (int i = 0; i < waitlist.size(); i++) {
            String faculty = waitlist.get(i).getFaculty();
            Date date = waitlist.get(i).getDate();
            seats = waitlist.get(i).getSeats();
            boolean result = ReservationQueries.addReservationEntry(faculty, date, seats);
            if (result) {
                WaitlistQueries.deleteWaitlistEntry(faculty, date);
                JOptionPane.showMessageDialog(this, "This room was reserved for a faculty!");
            }
        }
        fillRooms();
    }//GEN-LAST:event_jButtonRoomSeatsActionPerformed


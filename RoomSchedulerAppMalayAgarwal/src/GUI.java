    private void jButtonRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRoomActionPerformed
        String room = jComboBoxRoom.getSelectedItem().toString();
        RoomQueries.dropRoom(room);
        ReservationQueries.deleteReservation(room);
        JOptionPane.showMessageDialog(this, "Room dropped!");
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        boolean removed = false;
        for (int i = 0; i < waitlist.size(); i++) {
            String faculty = waitlist.get(i).getFaculty();
            Date date = waitlist.get(i).getDate();
            int seats = waitlist.get(i).getSeats();
            boolean result = ReservationQueries.addReservationEntry(faculty, date, seats);
            if (result) {
                removed = true;
                WaitlistQueries.deleteWaitlistEntry(faculty, date);
                JOptionPane.showMessageDialog(this, "A room was reserved for a faculty!");
            }
        }
        if (removed == false) 
            JOptionPane.showMessageDialog(this, "Couldn't reserve a room for the faculty!");
    }//GEN-LAST:event_jButtonRoomActionPerformed

    private void jButtonCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCancelActionPerformed
        String faculty = jComboBoxCancelFaculty.getSelectedItem().toString();
        Date date = Date.valueOf(jComboBoxCancelDate.getSelectedItem().toString());
        ReservationQueries.cancelReservation(faculty, date);
        WaitlistQueries.cancelWaitlistEntry(faculty, date);
        JOptionPane.showMessageDialog(this, "Reservation Canceled!");
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        for (int i = 0; i < waitlist.size(); i++) {
            faculty = waitlist.get(i).getFaculty();
            date = waitlist.get(i).getDate();
            int seats = waitlist.get(i).getSeats();
            boolean result = ReservationQueries.addReservationEntry(faculty, date, seats);
            if (result) {
                WaitlistQueries.deleteWaitlistEntry(faculty, date);
                JOptionPane.showMessageDialog(this, "A room was reserved for a faculty!");
            }
        }
    }//GEN-LAST:event_jButtonCancelActionPerformed


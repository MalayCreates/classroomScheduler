    public void fillWaitlist() {
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        listStatusWaitlist.removeAllElements();
        for (int i = 0; i < waitlist.size(); i++) {
            listStatusWaitlist.addElement("Faculty: " + waitlist.get(i).getFaculty() + "    Date: " + waitlist.get(i).getDate() + "   Seats: " + waitlist.get(i).getSeats());
        }
    }
    
    public void fillRooms() {
        ArrayList<String> room = new ArrayList<>();
        ArrayList<RoomEntry> rooms = RoomQueries.getAllPossibleRooms();
        for (int i = 0; i < rooms.size(); i++) {
            room.add(rooms.get(i).getName());
        }
        jComboBoxRoom.setModel(new javax.swing.DefaultComboBoxModel(room.toArray()));
    }
    
    public void fillDateBox() {
        jComboBoxDateReserve.setModel(new javax.swing.DefaultComboBoxModel(Dates.getAllDates().toArray()));
        jComboBoxDateStatus.setModel(new javax.swing.DefaultComboBoxModel(Dates.getAllDates().toArray()));
    }
    
    public void fillFacultyBox() {
        jComboBoxFacultyReserve.setModel(new javax.swing.DefaultComboBoxModel(Faculty.getFacultyList().toArray()));
        jComboBoxFaculty.setModel(new javax.swing.DefaultComboBoxModel(Faculty.getFacultyList().toArray()));
        jComboBoxCancelFaculty.setModel(new javax.swing.DefaultComboBoxModel(Faculty.getFacultyList().toArray()));
        
    }
    
    public void fillDateCancel() {
        ArrayList<Date> dates = new ArrayList<>();
        ArrayList<ReservationEntry> reserve = ReservationQueries.getReservationsByFaculty(jComboBoxCancelFaculty.getSelectedItem().toString());
        for (int i = 0; i < reserve.size(); i++) {
            dates.add(reserve.get(i).getDate());
        }
        jComboBoxCancelDate.setModel(new javax.swing.DefaultComboBoxModel(dates.toArray()));
    }
